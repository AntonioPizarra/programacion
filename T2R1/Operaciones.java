public class Operaciones{ 
    public static void main(String[] args) { 
        int x = 144 ;
        int y = 999 ;

        double suma ;
        suma = x + y ;
        System.out.println("x + y = "+ suma) ;

        double resta ;
        resta = x - y ;
        System.out.println("x - y = "+ resta) ;

        double mult ;
        mult = x * y ;
        System.out.println("x * y = "+ mult) ;

        double div ;
        div = x / y ;
        System.out.println("x / y = "+ div) ;

    }
}
